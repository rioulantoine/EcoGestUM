  <!-- Section engagements -->
  <section class="politique_engagements">
    <h2 class="politique_subtitle">Les engagements</h2>
    <ul class="politique_list">
      <li>
        <strong>1 – Recyclage total</strong><br>
        Nous nous engageons à recycler 100&nbsp;% des équipements électroniques et matériels pédagogiques obsolètes sur l’ensemble de nos campus d’ici 2030, en partenariat avec des filières locales de revalorisation.
      </li>
      <li>
        <strong>2 – ÉcoGestUM Actif</strong><br>
        Chaque année, nous ambitionnons d’augmenter de 20&nbsp;% le nombre d’objets déposés sur la plateforme EcoGestUM, afin de favoriser leur réutilisation et diminuer notre empreinte carbone tout en fournissant un accès gratuit à des ressources pouvant leur être utile.
      </li>
      <li>
        <strong>3 – Tri zéro déchets</strong><br>
        Tous les services universitaires devront adopter un plan de tri sélectif standardisé, incluant papiers, plastiques, cartons, avec suivi trimestriel des performances.
      </li>
      <li>
        <strong>4 – Sensibilisation continue</strong><br>
        Nous organiserons chaque semestre des ateliers et challenges étudiants sur le thème du recyclage, afin de promouvoir les pratiques écoresponsables au sein de la communauté universitaire et organismes réguliers des événements ouverts à tous sur le thème du recyclage et des impacts des déchets sur l’environnement.
      </li>
      <li>
        <strong>5 – Transparence et reporting</strong><br>
        Une fois par an, nous publierons un rapport détaillé sur le volume d’objets recyclés, les économies réalisées et les impacts environnementaux accessibles sur la page <a href="<?php echo getenv('BASE_URL'); ?>statistiques" target="_blank" class="politique_statlink">statistiques</a>.
      </li>
    </ul>
  </section>
  <!-- Objectifs -->
  <section class="politique_objectifs">
    <h2 class="politique_subtitle">Objectifs 2030 – 2050 <a style="color: #d35400; font-style: italic;" href ="<?php echo getenv('BASE_URL'); ?>objectifs" >(objectifs du moment)</a></h2>
    <ul>
      <li>Atteindre un taux de recyclage et réemploi supérieur à 90&nbsp;% des objets collectés sur l’ensemble des campus.</li>
      <li>Intégrer systématiquement des modules de sensibilisation au recyclage dans toutes les formations.</li>
      <li>Développer des partenariats avec des acteurs locaux pour renforcer l’efficacité et la portée de nos actions.</li>
      <li>Promouvoir un modèle de gestion durable et exemplaire, inspirant d’autres institutions et partenaires.</li>
    </ul>
  </section>

  <!-- Conseils pratiques -->
  <section class="politique_conseils">
    <h2 class="politique_subtitle">Conseils pour adopter des pratiques responsables</h2>
    <p>Pour chaque étudiant, enseignant ou personnel administratif, nous recommandons&nbsp;:</p>
    <ul>
      <li>Anticiper le cycle de vie des objets&nbsp;: réfléchir à la durée de vie selon l’utilisation, la réutilisation possible et le recyclage possible avant l’acquisition de matériel ou de fournitures.</li>
      <li>Trier correctement les déchets&nbsp;: utiliser les poubelles de tri sélectif pour papiers, plastiques, cartons...</li>
      <li>Favoriser le réemploi&nbsp;: privilégier le matériel de seconde main, les échanges internes entre services.</li>
      <li>Limiter le gaspillage&nbsp;: réparer, recycler, ou donnez les objets grâce à formulaire de don en interne.</li>
      <li>Participer aux actions de sensibilisation&nbsp;: assister aux ateliers et challenges organisés sur le campus pour comprendre l’impact environnemental de nos comportements.</li>
    </ul>
  </section>
